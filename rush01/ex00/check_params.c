int	check_params(int p[16])
{
	int	x;
	int	flag;

	x = 0;
	flag = 0;
	while (x < 4)
	{
		if (!(p[x] + p[x + 4] > 2 && p[x + 8] + p[x + 12] > 2))
			flag = 1;
		if (!(p[x] + p[x + 4] < 6 && p[x + 8] + p[x + 12] < 6))
			flag = 1;
		x++;
	}
	if (flag == 1)
		return (-1);
	else
		return (0);
}
